import java.io.*;
class AddIntegers
{
    public static void main(String args[]) throws IOException   //throws IOException is not necessary in the case of using try catch block
    {
        DataInputStream d=new DataInputStream(System.in);
        System.out.print("Enter first number:");
        int a=Integer.parseInt(d.readLine());
        System.out.print("Enter second number:");
        int b=Integer.parseInt(d.readLine());
        int c=a+b;
        System.out.print("The sum of two numbers is:"+c);
    }
}
